#3. TYPE CONVERSION
#a. Implicit Type Conversion
num1 = "10"
num2 = "20"
print(num1 + num2)  # Concatenation of strings
num1 = int(num1)  # Convert string to integer
num2 = int(num2)  # Convert string to integer
print(num1 + num2)  # Addition of integers
#b. Explicit Type Conversion
num1 = "10"
num2 = "20"
num1 = int(num1)  # Convert string to integer
num2 = int(num2)  # Convert string to integer
print(num1 + num2)  # Addition of integers
#c.
num1 = 10
num2 = 20
num1 = str(num1)  # Convert integer to string
num2 = str(num2)  # Convert integer to string
print(num1 + num2)  # Concatenation of strings
#d.
num1 = 10
num2 = 20
num1 = float(num1)  # Convert integer to float
num2 = float(num2)  # Convert integer to float
print(num1 + num2)  # Addition of floats
#e.
num1 = 10.5
num2 = 20.5
print(num1+num2)
num1 = int(num1)  # Convert float to integer
num2 = int(num2)  # Convert float to integer
print(num1 + num2)  # Addition of integers
#f.
num1 = 10.5
num2 = 20.5
num1 = str(num1)  # Convert float to string
num2 = str(num2)  # Convert float to string
print(num1 + num2)  # Concatenation of strings